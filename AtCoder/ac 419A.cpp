#include <bits/stdc++.h>
#define endl '\n'
using namespace std;

int main() {
	// ios::sync_with_stdio(false); cin.tie(0);
	string s;
	cin >> s;
	if (s == "red") {
		cout << "SSS" << endl;
	} else if (s == "blue") {
		cout << "FFF" << endl;
	} else if (s == "green") {
		cout << "MMM" << endl;
	} else {
		cout << "Unknown" << endl;
	}
}
