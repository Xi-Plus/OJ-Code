#include <iostream>
using namespace std;
int main(){
    long long int v=299792458;
    cout<<"1 Light-second(LS) is "<<v<<" metres."<<endl;
    cout<<"1 Light-minute(LM) is "<<v*60<<" metres."<<endl;
    cout<<"1 Light-hour(LH) is "<<v*3600<<" metres."<<endl;
    cout<<"1 Light-day(LD) is "<<v*86400<<" metres."<<endl;
    cout<<"1 Light-week(LW) is "<<v*86400*7<<" metres."<<endl;
    cout<<"1 Light-year(LY) is "<<v*86400*365<<" metres."<<endl;
}